package com.hp.sdf.ngp.model;

import java.io.Serializable;
import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

@Entity
@Table
public class CommentsSensorWord implements Serializable {
	private static final long serialVersionUID = -7897778912353336789L;

	private Long id;
	private String sensorWord;
	private Date updateDate;
	private String operatorId;

	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	@Column
	public String getSensorWord() {
		return sensorWord;
	}

	public void setSensorWord(String sensorWord) {
		this.sensorWord = sensorWord;
	}

	@Temporal(TemporalType.TIMESTAMP)
	@Column
	public Date getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	@Column
	public String getOperatorId() {
		return operatorId;
	}

	public void setOperatorId(String operatorId) {
		this.operatorId = operatorId;
	}

}

